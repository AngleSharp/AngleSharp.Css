namespace AngleSharp.Css
{
    using System;

    /// <summary>
    /// A collection of units used in CSS.
    /// </summary>
    public static class UnitNames
    {
        /// <summary>
        /// Kilohertz.
        /// </summary>
        public static readonly String Khz = "khz";

        /// <summary>
        /// Hertz.
        /// </summary>
        public static readonly String Hz = "hz";

        /// <summary>
        /// Milliseconds.
        /// </summary>
        public static readonly String Ms = "ms";

        /// <summary>
        /// Seconds.
        /// </summary>
        public static readonly String S = "s";

        /// <summary>
        /// Turns.
        /// </summary>
        public static readonly String Turn = "turn";

        /// <summary>
        /// Radiants.
        /// </summary>
        public static readonly String Rad = "rad";

        /// <summary>
        /// Grads.
        /// </summary>
        public static readonly String Grad = "grad";

        /// <summary>
        /// Degrees.
        /// </summary>
        public static readonly String Deg = "deg";

        /// <summary>
        /// Big Ms.
        /// </summary>
        public static readonly String Em = "em";

        /// <summary>
        /// Big Xs.
        /// </summary>
        public static readonly String Ex = "ex"; 

        /// <summary>
        /// Centimeters.
        /// </summary>
        public static readonly String Cm = "cm";

        /// <summary>
        /// Millimeters.
        /// </summary>
        public static readonly String Mm = "mm";

        /// <summary>
        /// Inches.
        /// </summary>
        public static readonly String In = "in";

        /// <summary>
        /// Pixels.
        /// </summary>
        public static readonly String Px = "px";

        /// <summary>
        /// Chars.
        /// </summary>
        public static readonly String Ch = "ch";

        /// <summary>
        /// Viewport-Width.
        /// </summary>
        public static readonly String Vw = "vw";

        /// <summary>
        /// Viewport-Height.
        /// </summary>
        public static readonly String Vh = "vh";

        /// <summary>
        /// Viewport-Minimum (Width / Height).
        /// </summary>
        public static readonly String Vmin = "vmin";

        /// <summary>
        /// Viewport-Maximum (Width / Height).
        /// </summary>
        public static readonly String Vmax = "vmax";

        /// <summary>
        /// Dots per inch.
        /// </summary>
        public static readonly String Dpi = "dpi";

        /// <summary>
        /// Dots per centimeter.
        /// </summary>
        public static readonly String Dpcm = "dpcm";

        /// <summary>
        /// Dots per pixel.
        /// </summary>
        public static readonly String Dppx = "dppx";

        /// <summary>
        /// Points.
        /// </summary>
        public static readonly String Pt = "pt";

        /// <summary>
        /// Remainings.
        /// </summary>
        public static readonly String Rem = "rem";

        /// <summary>
        /// Pieces.
        /// </summary>
        public static readonly String Pc = "pc";

        /// <summary>
        /// Percent.
        /// </summary>
        public static readonly String Percent = "%";
    }
}
