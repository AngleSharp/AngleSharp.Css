namespace AngleSharp.Css.Dom
{
    /// <summary>
    /// An enumeration with font variant transformations.
    /// </summary>
    public enum FontVariant : byte
    {
        /// <summary>
        /// Specifies a normal font face.
        /// </summary>
        Normal,
        /// <summary>
        /// Specifies a font that is labeled as a small-caps font. 
        /// </summary>
        SmallCaps
    }
}
