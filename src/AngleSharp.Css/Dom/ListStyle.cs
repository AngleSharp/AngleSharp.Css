namespace AngleSharp.Css.Dom
{
    /// <summary>
    /// An enumeration over possible list styles.
    /// </summary>
    public enum ListStyle : byte
    {
        /// <summary>
        /// No list style at all.
        /// </summary>
        None,
        /// <summary>
        /// A filled circle (default value)
        /// </summary>
        Disc,
        /// <summary>
        /// A hollow circle
        /// </summary>
        Circle,
        /// <summary>
        /// A filled square
        /// </summary>
        Square,
        /// <summary>
        /// Han decimal numbers, Beginning with 1.
        /// </summary>
        Decimal,
        /// <summary>
        /// Decimal numbers, Padded by initial zeros, E.g. 01, 02, 03, ... 98, 99
        /// </summary>
        DecimalLeadingZero,
        /// <summary>
        /// Lowercase roman numerals, E.g.i, ii, iii, iv, v, ...
        /// </summary>
        LowerRoman,
        /// <summary>
        /// Uppercase roman numerals, E.g.I, II, III, IV, V ...
        /// </summary>
        UpperRoman,
        /// <summary>
        /// Lowercase classical Greek, alpha, beta, gamma…, E.g.α, β, γ ...
        /// </summary>
        LowerGreek,
        /// <summary>
        /// Lowercase ASCII letters, E.g.a, b, c, ... z
        /// </summary>
        LowerLatin,
        /// <summary>
        /// Uppercase ASCII letters, E.g.A, B, C, ... Z
        /// </summary>
        UpperLatin,
        /// <summary>
        /// Traditional Armenian numbering, (ayb/ayp, ben/pen, gim/keem ... )
        /// </summary>
        Armenian,
        /// <summary>
        /// Traditional Georgian numbering, E.g.an, ban, gan, ... he, tan, in…
        /// </summary>
        Georgian,
        /// <summary>
        /// Kannada numbering.
        /// </summary>
        Kannada,
        /// <summary>
        /// Traditional Chinese formal numbering.
        /// </summary>
        TradChineseFormal,
        /// <summary>
        /// Traditional Chinese informal numbering.
        /// </summary>
        TradChineseInformal,
        /// <summary>
        /// Han decimal numbers.
        /// </summary>
        CjkDecimal,
        /// <summary>
        /// Arabic-Indic numbers.
        /// </summary>
        ArabicIndic,
        /// <summary>
        /// Bengali numbering.
        /// </summary>
        Bengali,
        /// <summary>
        /// Cambodian/Khmer numbering.
        /// </summary>
        Cambodian,
        /// <summary>
        /// Han "Earthly Branch" ordinals.
        /// </summary>
        CjkEarthlyBranch,
        /// <summary>
        /// Han "Heavenly Stem" ordinals.
        /// </summary>
        CjkHeavenlyStem,
        /// <summary>
        /// Devanagari numbering.
        /// </summary>
        Devanagari,
        /// <summary>
        /// Ethiopic numbering.
        /// </summary>
        EthiopicNumeric,
        /// <summary>
        /// Gurmukhi numbering.
        /// </summary>
        Gurmukhi,
        /// <summary>
        /// Traditional Hebrew numbering.
        /// </summary>
        Hebrew,
        /// <summary>
        /// Gujarati numbering.
        /// </summary>
        Gujarati,
        /// <summary>
        /// Dictionary-order hiragana lettering.
        /// </summary>
        Hiragana,
        /// <summary>
        /// Iroha-order hiragana lettering.
        /// </summary>
        IrohaHiragana,
        /// <summary>
        /// Japanese informal numbering.
        /// </summary>
        JapaneseInformal,
        /// <summary>
        /// Japanese formal numbering to be used in legal or financial documents. The kanjis are designed so that they can't be modified to look like another correct one.
        /// </summary>
        JapaneseFormal,
        /// <summary>
        /// Iroha-order katakana lettering.
        /// </summary>
        KatakanaIroha,
        /// <summary>
        /// Dictionary-order katakana lettering.
        /// </summary>
        Katakana,
        /// <summary>
        /// Korean hangul numbering.
        /// </summary>
        KoreanHangulFormal,
        /// <summary>
        /// Formal Korean Han numbering.
        /// </summary>
        KoreanHanjaFormal,
        /// <summary>
        /// Korean hanja numbering.
        /// </summary>
        KoreanHanjaInformal,
        /// <summary>
        /// Laotian numbering.
        /// </summary>
        Lao,
        /// <summary>
        /// Uppercase Armenian numbering.
        /// </summary>
        UpperArmenian,
        /// <summary>
        /// Lowercase Armenian numbering.
        /// </summary>
        LowerArmenian,
        /// <summary>
        /// Malayalam numbering.
        /// </summary>
        Malayalam,
        /// <summary>
        /// Mongolian numbering.
        /// </summary>
        Mongolian,
        /// <summary>
        /// Myanmar (Burmese) numbering.
        /// </summary>
        Myanmar,
        /// <summary>
        /// Oriya numbering.
        /// </summary>
        Oriya,
        /// <summary>
        /// Persian numbering.
        /// </summary>
        Persian,
        /// <summary>
        /// Simplified Chinese formal numbering.
        /// </summary>
        SimpChineseFormal,
        /// <summary>
        /// Simplified Chinese informal numbering.
        /// </summary>
        SimpChineseInformal,
        /// <summary>
        /// Tamil numbering.
        /// </summary>
        Tamil,
        /// <summary>
        /// Telugu numbering.
        /// </summary>
        Telugu,
        /// <summary>
        /// Thai numbering.
        /// </summary>
        Thai,
        /// <summary>
        /// Tibetan numbering.
        /// </summary>
        Tibetan,
        /// <summary>
        /// Symbol indicating that a disclosure widget, like &lt;details&gt; is closed.
        /// </summary>
        DisclosureClosed,
        /// <summary>
        /// Symbol indicating that a disclosure widget such as &lt;details&gt; is opened.
        /// </summary>
        DisclosureOpen,
    }
}
