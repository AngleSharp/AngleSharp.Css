namespace AngleSharp.Css.Dom
{
    /// <summary>
    /// An enumeration with the various text decoration styles.
    /// </summary>
    public enum TextDecorationStyle : byte
    {
        /// <summary>
        /// Draws a single line.
        /// </summary>
        Solid,
        /// <summary>
        /// Draws a double line.
        /// </summary>
        Double,
        /// <summary>
        /// Draws a dotted line.
        /// </summary>
        Dotted,
        /// <summary>
        /// Draws a dashed line.
        /// </summary>
        Dashed,
        /// <summary>
        /// Draws a wavy line.
        /// </summary>
        Wavy
    }
}
