namespace AngleSharp.Css.Dom
{
    /// <summary>
    /// Potential flex directions.
    /// </summary>
    public enum FlexDirection
    {
        /// <summary>
        /// Same direction as inline axis, i.e., from left to right.
        /// </summary>
        Row,
        /// <summary>
        /// Inverse direction as inine axis, i.e., from right to left.
        /// </summary>
        RowReverse,
        /// <summary>
        /// Same direction as block axis, i.e., from top to bottom.
        /// </summary>
        Column,
        /// <summary>
        /// Inverse direction as block axis, i.e., from bottom to top.
        /// </summary>
        ColumnReverse,
    }
}
