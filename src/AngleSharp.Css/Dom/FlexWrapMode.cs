namespace AngleSharp.Css.Dom
{
    /// <summary>
    /// Defines the values for flex wrapping.
    /// </summary>
    public enum FlexWrapMode
    {
        /// <summary>
        /// No wrap is being done.
        /// </summary>
        NoWrap,
        /// <summary>
        /// Normal wrapping is enabled.
        /// </summary>
        Wrap,
        /// <summary>
        /// Same as wrap with cross-start and cross-end swapped.
        /// </summary>
        WrapReverse,
    }
}
